    /// </summary>
    UByte2,

    /// <summary>
    /// Three unsigned 8-bit values.
    /// </summary>
    UByte3,

    /// <summary>
    /// Four unsigned 8-bit values.
    /// </summary>
    UByte4,

    /// <summary>
    /// One signed 8-bit value.
    /// </summary>
    Byte1,

    /// <summary>
    /// Two signed 8-bit values.
    /// </summary>
    Byte2,

    /// <summary>
    /// Three signed 8-bit values.
    /// </summary>
    Byte3,

    /// <summary>
    /// Four signed 8-bit values.
    /// </summary>
    Byte4,

    /// <summary>
    /// One unsigned normalized 8-bit value.
    /// </summary>
    UByte1Normalized,

    /// <summary>
    /// Two unsigned normalized 8-bit values.
    /// </summary>
    UByte2Normalized,

    /// <summary>
    /// Three unsigned normalized 8-bit values.
    /// </summary>
    UByte3Normalized,

    /// <summary>
    /// Four unsigned normalized 8-bit values.
    /// </summary>
    UByte4Normalized,

