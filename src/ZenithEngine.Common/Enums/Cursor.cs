namespace ZenithEngine.Common.Enums;

public enum Cursor
{
    /// <summary>
    /// The default arrow cursor.
    /// </summary>
    Arrow,

    /// <summary>
    /// The text input I-beam cursor shape.
    /// </summary>
    TextInput,

    /// <summary>
    /// The crosshair shape.
    /// </summary>
    ResizeAll,

    /// <summary>
    /// The horizontal resize arrow shape.
    /// </summary>
    ResizeNS,

    /// <summary>
    /// The vertical resize arrow shape.
    /// </summary>
    ResizeWE,

    /// <summary>
    /// The Bottom-left to top-right diagonal resize arrow shape.
    /// </summary>
    ResizeNESW,

    /// <summary>
    /// The Bottom-right to top-left diagonal resize arrow shape.
    /// </summary>
    ResizeNWSE,

    /// <summary>
    /// The Hand shape.
    /// </summary>
    Hand,

    /// <summary>
    /// The Not-allowed shape.
    /// </summary>
    NotAllowed
}
